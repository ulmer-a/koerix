const char *strerror(unsigned err)
{
    if (err < sizeof(err_messages))
        return err_messages[err];
    return "Unknown error";
}

const char *strexcept(unsigned exc)
{
  if (exc < sizeof(exceptions))
    return exceptions[exc];
  return "unknown";
}
